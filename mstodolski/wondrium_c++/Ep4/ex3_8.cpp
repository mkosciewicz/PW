//  Comparisons: equality/inequality
#include <iostream>
using namespace std;
 
int main() {
   int numFish = 1, numCats = 2, numDogs = 2;
   cout << (numFish < numCats) << endl;
   cout << (numFish != numCats) << endl;
   cout << (numCats > numDogs) << endl;
   cout << (numCats != numDogs) << endl;

    return 0;
}